mean_body_fat <- mean(body_fat)
median_body_fat <- median(body_fat)
sd_body_fat <- sd(body_fat)
cat("Age:\n")
cat("Mean:", mean_age, "\n")
cat("Median:", median_age, "\n")
cat("Standard Deviation:", sd_age, "\n\n")

cat("Body Fat Percentage:\n")
cat("Mean:", mean_body_fat, "\n")
