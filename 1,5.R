age <- c(23, 23, 27, 27, 39, 41, 47, 49, 50, 52, 54, 54, 56, 57, 58, 60, 61, 61)
body_fat <- c(9.5, 26.5, 7.8, 17.8, 32.4, 28.8, 22.4, 27.2, 31.2, 34.6, 42.5, 28.8, 33.4, 30.2, 34.1, 32.5, 41.2, 35.7)
mean_age <- mean(age)
median_age <- median(age)
